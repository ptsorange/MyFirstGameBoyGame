void setSpritePos(int sprite, int x, int y, int w, int h) {
  int i = 0;
  for (int w1 = 0; w1 < w; w1++) {
    for (int h1 = 0; h1 < h; h1++) {
      move_sprite(sprite + i, x+(h1*8), y+(w1*8));
      i++;
    }
  }
}

void main(void) {
  uint8_t px = 50;
  uint8_t py = 50;

  set_sprite_data(0, 4, player_tile);
  set_sprite_data(4, 4, floor_tile);

  for (int i = 0; i < 8; i++) {
    set_sprite_tile(i, i);
  }

  SHOW_SPRITES;

  while (1) {
    uint8_t input = joypad();
    if (input & J_LEFT)
      px--;
    if (input & J_RIGHT)
      px++;
    if (input & J_UP)
      py--;
    if (input & J_DOWN)
      py++;

    setSpritePos(0,px,py,2,2);

    wait_vbl_done();
  }
}